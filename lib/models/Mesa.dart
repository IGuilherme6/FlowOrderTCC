
class Mesa {
  late String _uid;
  late String _nome;
  late int _numero;


  String get uid => _uid;

  set uid(String value) {
    _uid = value;
  }

  String get nome => _nome;

  set nome(String value) {
    _nome = value;
  }

  int get numero => _numero;

  set numero(int value) {
    _numero = value;
  }
}
